package com.andrey.tugas2_pkkm;

public class ModelBahasa {
    private  int id;
    private  String bahasa_indonesia, bahasa_sunda;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBahasa_indonesia() {
        return bahasa_indonesia;
    }

    public void setBahasa_indonesia(String bahasa_indonesia) {
        this.bahasa_indonesia = bahasa_indonesia;
    }

    public String getBahasa_sunda() {
        return bahasa_sunda;
    }

    public void setBahasa_sunda(String bahasa_sunda) {
        this.bahasa_sunda = bahasa_sunda;
    }
}
